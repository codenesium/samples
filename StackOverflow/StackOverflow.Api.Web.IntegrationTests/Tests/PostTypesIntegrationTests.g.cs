			              .UseEnvironment("Production")
			              .UseStartup<TestStartup>();
			TestServer testServer = new TestServer(builder);

			var client = new ApiClient(testServer.CreateClient());
			JWTHelper jwtHelper = new JWTHelper();
			client.SetBearerToken(jwtHelper.GenerateBearerToken(
									  "defaultJWTPassword",
									  "https://www.codenesium.com",
									  "https://www.codenesium.com",
									  "test@test.com",
									  "Passw0rd$"));
			var mapper = new ApiPostTypesServerModelMapper();
			ApplicationDbContext context = testServer.Host.Services.GetService(typeof(ApplicationDbContext)) as ApplicationDbContext;
			IPostTypesService service = testServer.Host.Services.GetService(typeof(IPostTypesService)) as IPostTypesService;
			ApiPostTypesServerResponseModel model = await service.Get(1);

			ApiPostTypesClientRequestModel request = mapper.MapServerResponseToClientRequest(model);
			request.SetProperties("B");

			UpdateResponse<ApiPostTypesClientResponseModel> updateResponse = await client.PostTypesUpdateAsync(model.Id, request);

			context.Entry(context.Set<PostTypes>().ToList()[0]).Reload();
			updateResponse.Record.Should().NotBeNull();
			updateResponse.Success.Should().BeTrue();
			updateResponse.Record.Id.Should().Be(1);
			context.Set<PostTypes>().ToList()[0].RwType.Should().Be("B");

			updateResponse.Record.Id.Should().Be(1);
			updateResponse.Record.RwType.Should().Be("B");
		}

