using System.Threading.Tasks;

namespace PetShippingNS.Api.Services
{
	public class ApiPipelineStepStatusServerRequestModelValidator : AbstractValidator<ApiPipelineStepStatusServerRequestModel>, IApiPipelineStepStatusServerRequestModelValidator
	{
		private int existingRecordId;

		protected IPipelineStepStatusRepository PipelineStepStatusRepository { get; private set; }

		public ApiPipelineStepStatusServerRequestModelValidator(IPipelineStepStatusRepository pipelineStepStatusRepository)
		{
			this.PipelineStepStatusRepository = pipelineStepStatusRepository;
		}

		public async Task<ValidationResult> ValidateAsync(ApiPipelineStepStatusServerRequestModel model, int id)
		{
			this.existingRecordId = id;
			return await this.ValidateAsync(model);
		}

		public async Task<ValidationResult> ValidateCreateAsync(ApiPipelineStepStatusServerRequestModel model)
		{
			this.NameRules();
			return await this.ValidateAsync(model);
		}

		public async Task<ValidationResult> ValidateUpdateAsync(int id, ApiPipelineStepStatusServerRequestModel model)
		{
			this.NameRules();
			return await this.ValidateAsync(model, id);
		}

		public async Task<ValidationResult> ValidateDeleteAsync(int id)
		{
			return await Task.FromResult<ValidationResult>(new ValidationResult());
		}

		public virtual void NameRules()
		{
			this.RuleFor(x => x.Name).NotNull().WithErrorCode(ValidationErrorCodes.ViolatesShouldNotBeNullRule);
			this.RuleFor(x => x.Name).Length(0, 128).WithErrorCode(ValidationErrorCodes.ViolatesLengthRule);
		}
	}
}

