namespace CADNS.Api.Services
{
	public class ApiVehCapabilityServerRequestModelValidator : AbstractValidator<ApiVehCapabilityServerRequestModel>, IApiVehCapabilityServerRequestModelValidator
	{
		private int existingRecordId;

		protected IVehCapabilityRepository VehCapabilityRepository { get; private set; }

		public ApiVehCapabilityServerRequestModelValidator(IVehCapabilityRepository vehCapabilityRepository)
		{
			this.VehCapabilityRepository = vehCapabilityRepository;
		}

		public async Task<ValidationResult> ValidateAsync(ApiVehCapabilityServerRequestModel model, int id)
		{
			this.existingRecordId = id;
			return await this.ValidateAsync(model);
		}

		public async Task<ValidationResult> ValidateCreateAsync(ApiVehCapabilityServerRequestModel model)
		{
			this.NameRules();
			return await this.ValidateAsync(model);
		}

		public async Task<ValidationResult> ValidateUpdateAsync(int id, ApiVehCapabilityServerRequestModel model)
		{
			this.NameRules();
			return await this.ValidateAsync(model, id);
		}

		public async Task<ValidationResult> ValidateDeleteAsync(int id)
		{
			return await Task.FromResult<ValidationResult>(new ValidationResult());
		}

		public virtual void NameRules()
		{
			this.RuleFor(x => x.Name).NotNull().WithErrorCode(ValidationErrorCodes.ViolatesShouldNotBeNullRule);
			this.RuleFor(x => x.Name).Length(0, 128).WithErrorCode(ValidationErrorCodes.ViolatesLengthRule);
		}
	}
}

