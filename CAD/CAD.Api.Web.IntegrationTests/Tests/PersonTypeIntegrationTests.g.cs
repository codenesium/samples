		public virtual async void TestBulkInsert()
		{
			var builder = new WebHostBuilder()
			              .UseEnvironment("Production")
			              .UseStartup<TestStartup>();
			TestServer testServer = new TestServer(builder);
			var client = new ApiClient(testServer.CreateClient());
			ApplicationDbContext context = testServer.Host.Services.GetService(typeof(ApplicationDbContext)) as ApplicationDbContext;

			var model = new ApiPersonTypeClientRequestModel();
			model.SetProperties("B");
			var model2 = new ApiPersonTypeClientRequestModel();
			model2.SetProperties("C");
			var request = new List<ApiPersonTypeClientRequestModel>() {model, model2};
			CreateResponse<List<ApiPersonTypeClientResponseModel>> result = await client.PersonTypeBulkInsertAsync(request);

			result.Success.Should().BeTrue();
			result.Record.Should().NotBeNull();

			context.Set<PersonType>().ToList()[1].Name.Should().Be("B");

			context.Set<PersonType>().ToList()[2].Name.Should().Be("C");
		}

