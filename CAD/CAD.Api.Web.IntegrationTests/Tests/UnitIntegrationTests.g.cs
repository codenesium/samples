		[Fact]
		public virtual async void TestBulkInsert()
		{
			var builder = new WebHostBuilder()
			              .UseEnvironment("Production")
			              .UseStartup<TestStartup>();
			TestServer testServer = new TestServer(builder);
			var client = new ApiClient(testServer.CreateClient());

			JWTHelper jwtHelper = new JWTHelper();
			client.SetBearerToken(jwtHelper.GenerateBearerToken(
									  "defaultJWTPassword",
									  "https://www.codenesium.com",
									  "https://www.codenesium.com",
									  "test@test.com",
									  "Passw0rd$"));

			ApplicationDbContext context = testServer.Host.Services.GetService(typeof(ApplicationDbContext)) as ApplicationDbContext;

			var model = new ApiUnitClientRequestModel();
			model.SetProperties("B");
			var model2 = new ApiUnitClientRequestModel();
			model2.SetProperties("C");
			var request = new List<ApiUnitClientRequestModel>() {model, model2};
			CreateResponse<List<ApiUnitClientResponseModel>> result = await client.UnitBulkInsertAsync(request);

			result.Success.Should().BeTrue();
			result.Record.Should().NotBeNull();

			context.Set<Unit>().ToList()[1].CallSign.Should().Be("B");

			context.Set<Unit>().ToList()[2].CallSign.Should().Be("C");
		}

